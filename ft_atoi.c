int	ft_atoi(const char *nptr)
{
	int	i;
	int	s;
	int	res;
	int	count;

	i = 0;
	res = 0;
	s = 1;
	count = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
	{
		i++;
	}
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			s = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = nptr[i] - '0' + res * 10;
		i++;
	}
	return (s * res);
}
